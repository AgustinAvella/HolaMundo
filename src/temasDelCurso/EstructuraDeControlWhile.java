package temasDelCurso;

public class EstructuraDeControlWhile {

	public static void main(String[] args) {
		
		
		int i = 0;
		while ((i<5)) {
			
			if(i%2==0) {
				System.out.println("Valor de la variable i: "+" "+i+" es par");
			}else {
				System.out.println("Valor de la variable i: "+" "+i+" es impar");
			}
			i++;
		}
	}
}
		



